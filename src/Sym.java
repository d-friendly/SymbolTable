public class Sym {
    
    private String type; 

    /**
     * Intializes type and constructs Sym.
     * @param type
     */
    public Sym(String type){
        this.type = type;
    }

    
    /**
     * Gets the type of Sym
     * @return type
     */
    public String getType(){
        return type;
    }

    /**
     * returns type of Sym
     * @return 
     */
    public String toString(){
        return type;
    }
}
